#include "libft.h"

void	ft_strclr(char *s)
{
	int len;
	int i;

	i = 0;
	if (s)
	{
		len = ft_strlen(s);
		while (s[i] && i <= len)
		{
			s[i] = '\0';
			i++;
		}
	}
}
